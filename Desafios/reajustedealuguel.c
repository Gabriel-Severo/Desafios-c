//altera o mês para o seu índice equivalente
//Ex: Mês 0 -> Janeiro  11 -> Dezembro, então 12 -> Janeiro
int mesAlt(int nMes, int *ano, int *anoC, int *vezes2){
        if(*vezes2 >= 12){
			*anoC = *anoC + 1;
            *vezes2 = 0;
        }if(nMes >= 24){
        	nMes = nMes - 24;
        	*ano = *ano + 2;
		}
        else if(nMes >= 12){
            nMes = nMes - 12;
            *ano = *ano + 1;
        }

        return nMes;
}

//calcula o reajuste de acordo com o periodo e índice selecionado
void calcularAluguel(float taxa[][12], float aluguel, char mesesTexto[][10], int ano, int anoC, int mes, int opcPeriodico){
    float taxasAc = 0;
    int nMes = mes, vezes, vezes2, num;
    int cont1 = mes, cont2 = nMes;

    int qtdEx = (2019 - ano) * 12 + 5; //quantidade de execuções

    for (vezes = cont1; vezes < qtdEx; vezes+=opcPeriodico){
    	nMes = mesAlt(nMes, &ano, &anoC, &cont2);
        for(vezes2 = nMes; vezes2 < nMes + opcPeriodico; vezes2++){
            nMes = mesAlt(nMes, &ano, &anoC, &cont2);
            taxasAc = taxasAc + taxa[anoC][cont2];
            cont2++;
        }

        if(opcPeriodico == 3 && cont1 >= qtdEx - 2)
            break;
        else if(opcPeriodico == 6 && cont1 >= qtdEx - 5)
            break;
        else if(opcPeriodico == 12 && cont1 >= qtdEx - 12)
            break;
        else if(opcPeriodico == 24 && cont1 >= qtdEx - 24)
        	break;

        aluguel += aluguel * (taxasAc / 100);
        
        //condição para impressão
        if(nMes+opcPeriodico>=24){
        	num = nMes+opcPeriodico-24;
        	printf("\nReajuste em %s-%d\nVariação do índice %.2f%%\nValor Reajustado RS%.2f\n", mesesTexto[num], ano+2, taxasAc, aluguel);
        }else if(nMes+opcPeriodico>=12){
            num = nMes+opcPeriodico-12;
            printf("\nReajuste em %s-%d\nVariação do índice %.2f%%\nValor Reajustado RS%.2f\n", mesesTexto[num], ano+1, taxasAc, aluguel);
        }else
            printf("\nReajuste em %s-%d\nVariação do índice %.2f%%\nValor Reajustado RS%.2f\n", mesesTexto[nMes+opcPeriodico], ano, taxasAc, aluguel);
        cont1+=opcPeriodico;
        nMes+=opcPeriodico;
        taxasAc = 0;
    }
}

